#include <stdio.h>
#include "3-calc.h"
/**
 * op_add - add
 * @a: first int
 * @b: 2nd int
 * Return: Nothing.
 */
int op_add(int a, int b)
{
return (a + b);
}
/**
 * op_sub - sub
 * @a: first int
 * @b: 2nd int
 * Return: Nothing.
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mul - mul
 * @a: first int
 * @b: 2nd int
 * Return: Nothing.
 */
int op_mul(int a, int b)
{
return (a * b);
}
/**
 * op_div - div
 * @a: first int
 * @b: 2nd int
 * Return: Nothing.
 */
int op_div(int a, int b)
{
return (a / b);
}
/**
 * op_mod - mod
 * @a: first int
 * @b: 2nd int
 * Return: Nothing.
 */
int op_mod(int a, int b)
{
return (a % b);
}
